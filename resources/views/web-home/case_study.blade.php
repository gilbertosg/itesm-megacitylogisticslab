                <h2 class="small-heading">Sophisticated modeling</h2>
                <div class="row">
                    <div class="col-md-10 col-md-offset-1 col-xs-10 col-xs-offset-1">
                        <div class="row">
                            <div class="col-md-12 col-xs-12">

                                <p align="justify">
                                    In addition to helping companies use data they currently collect to analyze urban
                                    delivery routes in more detail, the MIT Megacity Logistics Lab is also developing
                                    more advanced modeling techniques. An example is a simulation of a megacity location
                                    that allows managers to move physical representations of resources, such as distribution
                                    centers, and see how such moves change the cost profile of a distribution network.
                                    The simulation functions as both a planning aid and a learning tool.
                                </p>

                                <p align="justify">
                                    As megacities continue to expand, and the growth in ecommerce adds another layer
                                    of complexity to urban distribution networks, models that help companies to streamline
                                    the last mile will become even more important.
                                </p>

