            <div class="clearfix full-height">
                <h2 class="small-heading">Why we do it</h2>
                <div class="row">
                    <div class="col-md-10 col-md-offset-1 col-xs-10 col-xs-offset-1">
                        <div class="row">
                            <div class="col-md-12 col-xs-12">

                                <p align="justify">
                                    Logistics is an enabler of quality of life in cities: it delivers goods and
                                    services to city dwellers enabling them to enjoy all the benefits of the urban
                                    environment. There are, however, three major drivers of increased complexity of
                                    urban logistics networks that calls for a new thinking on city logistics.
                                </p>

                                <p align="justify">
                                    First, urbanization is progressing at a high pace. While in 1950 only 54.5% of
                                    the population in developed countries was in urban areas, this number had risen
                                    to 77.7% by 2011 and is projected to reach 85.9% by 2050. Moreover, 25% of the
                                    world population and almost 60% of the world’s gross domestic product (GDP) will
                                    be found in the world’s 600 largest cities by 2025. Megacities, urban areas with
                                    over 10 million people, are an example of this new era of urbanization.
                                    Megacities allow us to focus in the most complex environments, but we aim for
                                    our work to be applied to urban areas of all sizes.
                                </p>

                                <p align="justify">
                                    Second, the growth of Internet and mobile phone based electronic commerce is
                                    triggering an increasing amount of direct shipments from manufacturers and retailers
                                    to individuals. Direct deliveries do not only increase complexity of last-mile urban
                                    transportation networks, they also lead to fragmentation of shipments and higher
                                    complexity and greater need for coordination between consumers, retailers and manufacturers
                                    to distribute goods efficiently. These changes in consumer buying behaviors are a challenge
                                    and an opportunity to design future urban logistics networks.
                                </p>

                                <p align="justify">
                                    Finally, on-going efforts from cities to invest in public transportation, limiting
                                    road access and parking spaces in favor of pedestrian and public transit infrastructure,
                                    disproportionally impact logistics operations. Since urban freight also generates an
                                    important share of congestion, pollution and other negative externalities, city
                                    logistics activities are always under pressure from regulatory actions. Private and
                                    public sector need to collaborate to find the right balance to improve the quality
                                    of life in the urban environment. Our work will bring together business, logistics
                                    and urban planning perspectives so we can develop technologies and policies that
                                    gets the most out of the limited urban space and infrastructure.
                                </p>

